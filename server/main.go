// function called at startup to load the config file or create one if it doesn't exist
func readConfig(configFilepath string) map[string]any {
	// Reads the Panopticon server configuration file from the provided path and returns a map of the keys and values it contains. If the file does not exist, one will be autogenerated.
	// check that the config file actually exists first, if not then we fill it in with the default template
	var cm map[string]any = make(map[string]any)

	if _, err := os.Stat(configFilepath); os.IsNotExist(err) {
		// config file doesn't exist
		configFile, createErr := os.Create(configFilepath)
		if createErr != nil {
			log.Panic("Error creating auto-generated config file")
		}
		defer configFile.Close()

		_, writeErr := configFile.WriteString("# Panopticon config - autogenerated by server due to missing config")
		if writeErr != nil {
			log.Panic("Error writing to auto-generated config file")
		}
		// TODO: make sure to update auto-generation with new config values
		configFile.WriteString("DATABASE_FILEPATH = panopticon.db")
		configFile.WriteString("REST_PORT = 4001")
		return readConfig(configFilepath) // recursive call to this same function now that we've created the file!
	} else if err != nil {
		log.Panic("Ran into an error determining if the file exists")
	}

	configFile, readErr := os.Open(configFilepath)
	if readErr != nil {
		log.Panic("Config file exists, but Panopticon was unable to read it.")
	}
	defer configFile.Close() // always make sure we're cleaning up after ourselves!

	scanner := bufio.NewScanner(configFile)

	for scanner.Scan() {
		line := scanner.Text()
		line = strings.Trim(line, " ") // trim whitespace from both ends of any config lines
		if len(line) > 0 {
			if string(line[0]) == "#" {
				// if a line begins with hashtag, it's a comment! ignore :)
				continue
			} else {
				splitLine := strings.Split(line, "=")
				key := strings.Trim(splitLine[0], " ")
				value := strings.Trim(splitLine[1], " ")
				cm[key] = value
			}
		}
	}
	return cm
}

